Paddy is the main agricultural crop in the province. Wayamba is the third largest paddy-producing area in Sri Lanka. Wayamba has a highly developed agricultural economy, growing a variety of fruits and vegetables, flowering plants, spices, oil-seeds in addition to the traditional plantation crops such as Coconut, Rubber and Rice. Rich soils and varied climate give Wayamba a potential for growing of virtually any crop.
