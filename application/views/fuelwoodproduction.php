                        <h3 class="name">Fuel wood</h3>
                        <p>More than 450 000 plant species have been identified worldwide approximately 3000 of these are used by human as source of fuel wood, tools and other feed stocks. About 300 plants plant species have been domesticated as crops for agriculture, of these, 60 species are major importance. Any wood used as a fuel is called as fuelwood. Fuelwood may be in different form such as firewood, charcoal, chips, sheets, pellets, and sawdust End product characteristics of fuel wood such as specific gravity, calorific value and ash content are the key factors of the demand equation of fuel wood in the market. Some processing operations are taken place to manipulate properties of fuel wood for value addition and end product optimizing to suit in industry utilization.<br></p>
                        <h3 class="name">Selection criteria of a fuelwood species</h3>
                        <p>There are specific characters those create species more attractive to use as a fuel wood species. Selection of species is based on the species potential as compared to other species, its performances at a tested site and for the kinds of quality of desired benefits produced. Only those species which are capable of meeting the desired production and other goals should be selected.<br></p>
