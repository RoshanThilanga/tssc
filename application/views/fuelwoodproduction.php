                    <div class="mb-md-30">
                        <h3 style="color: #8cc63f" class="year">Some important characters of a fuel wood species</h3>
                        <h3 class="name">Growth rate</h3>
                        <p>Growth refers vertical growth and increase of girth or diameter against time, results increased weight and/or volume. Fast growing species are the most suitable species for fuel wood production. Such species produce increased volume /weight of biomass within shorter period of time compared with other species under same environmental condition.<br></p>
                        <h3 class="name">Wider adaptation</h3>
                        <p>Wider adaptation refers species is distributed over wide range of soil and climatic conditions. Sri Lanka is an island has changing ecology resulted with 46 agro ecological regions. Widely adapted species are mostly preferable for fuel wood production in Sri Lanka.<br></p>
                        <h3 class="name">Nitrogen fixing ability</h3>
                        <p>It refers to the ability of a tree to fix atmospheric nitrogen. Nitrogen-fixing ability is an important aspect of the selection criteria of a fuelwood species as nitrogen is the major limiting factor of the tropical soil. Availability of nitrogen in particular soil heavily influences the growth rate of selected species hence nitrogen-fixing trees to have greater potential over other species in mass production.<br></p>
                        <h3 class="name">Ease of establish</h3>
                        <p>This refers to the regeneration ability of a species with multiple sources such as seeds by direct sawing and vegetatively propagated by cuttings. There may be some other methods of propagation other than these two methods. Those regeneration methods need to be less complicated and easy, which is not demand complicated nursery methods.<br></p>
                        <h3 class="name">Period of rotation</h3>
                        <p>Refers time required for maturity of fuel wood for harvesting. Shorter rotation is preferred in fuel wood plantations. Some instances shorter rotation species gives fuel wood distributed over time with greater volume/weight than other species.<br></p>
                        <h3 class="name">Free of pest and disease problems</h3>
                        <p>This refers tolerance of species for pest and disease condition in the growing environment. Species selected with high resistance to pest and disease conditions are preferable as fuelwood species.
                            <br>
                        </p>
