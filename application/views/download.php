                  <p>South-South Cooperation is a broad framework for collaboration among countries of the South within 
the political, economic, social, cultural, environmental, and technical domains. South-South Cooperation 
involves two or more developing countries, multi-lateral organizations or partners, and can take place on 
a bilateral, regional, sub-regional or interregional basis. This project is designed as a south-south 
cooperation project to be implemented through a trilateral cooperation modality. 
UNDP emphasizes the importance of South-South Cooperation in its Strategic Plan (2018-2021) as a 
welcomed contribution to global development and traditional aid flows. Trilateral South-South 
Cooperation is a unique approach in which three parties cooperate to implement innovative solutions. 
Projects are designed as dynamic platforms for exchange and partnership between developing countries. 
The underlying principle is that each country benefits from transfer of specific technical capacities, 
knowledge exchange and policy development. 
UNDP’s role in Trilateral South-South Cooperation project is to coordinate the exchange of knowledge 
and experience. UNDP also acts as a convener of international cooperation best practices in project 
formulation (i.e. integrating beneficiary country needs and development priorities, project partners’ 
ownership) and implementation (i.e. following standard project management and reporting procedures, 
ensuring quality capacity development activities, and partners’ equality in cooperation modalities).  
