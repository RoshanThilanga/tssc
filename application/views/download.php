                  <h3>Our Services </h3>
                  <h4>Capacity building and training </h4>
                  <p>Training of Provincial Council Officers 
The activities will focus on developing Provincial Councils’ capacities to manage the transition to RET for 
clean energy consumption and production: i.e. assess, set targets and monitor. It will bridge the data 
gap, by piloting a data collection system in the five provinces that feeds into the ENERGIS database for 
the agro-industry. <br>
 <b>Training of trainers in installation, operation & maintenance</b><br> 
The training courses will lead to the formulation of manual for operation and maintenance of specific 
system; for example, maintaining ph levels, feedstock, screening safety protocols, operation of 
machinery for biogas systems.  <br>
<b>Training on system design</b> <br>
The training courses will lead to curricula development on designing biogas and solar systems and 
advisory services for agro-industry (i.e. energy audits, sizing and system optimization, costing systems, 
biogas upgrading, etc.), including the formulation of online courses. In order to reinforce the 
South-South knowledge exchange, Chinese implementing partners (e.g. China Agricultural University) 
will adapt the training material to an E-Learning course that will be made accessible by ACCA21 on the 
South-South Cooperation Centre website 

<b>Pilot demonstrations </b>
Provincial stakeholder consultations will be used in identifying the priority areas for the application of 
renewable energy technologies in the agro-industry. With the priority areas identified, provincial 
councils will identify potentials sites for which feasibility studies will be carried out. These feasibility 
studies will be used in prioritizing sites for demonstration of renewable energy technologies  
<b>Data collection framework </b>
The project will support the Provincial Councils to collect and submit disaggregated data on energy 
consumption, waste generated and fertilizer used in the agro-industry to the ENERGIS. Support will be 
given through capacity building of provincial council officers and development of the ENERGIS database 
<b>Provincial Energy Plans </b>
The data collection framework will be used in developing energy plans for the agro industry for three 
provinces. Data generated through the framework will be used as a basis to screen data required for a 
provincial level baseline of energy consumption and GHG inventory (i.e. energy needs, generated wastes 
and uses of fertilizers). The baseline will allow comparison with energy savings and GHG emissions 
reductions achieved through introduction of RETs and support the definition of targets for provincial 
energy plan 
<b>Best Business Practices </b>
 
 
Through a match making platform which will help identify suppliers and facilitate business relationships 
to match potential demand for RETs in Sri Lanka agro-industry sector. Chinese partner will identify the Sri 
Lanka energy service providers demand in terms of access to technology hardware and develop an 
online catalogue of demand-driven biogas and solar PV products/appliances and technologies for 
agro-industry. 
Chinese partners will also organize and host trade fairs for RET technology with site visits for technology 
demonstration and business match-making 
 
<b>Joint Research Centers </b>
Through a set of collaborative research and consultations the project will mainstream biogas and solar 
potential for a transition to sustainable energy consumption and production patterns. Studies and 
consultation will consider: <br>
Why various technologies and technical systems have failed? <br>
Potential for biogas standardization. <br>
Socio-economic study on farmer behaviour, young farmer expectations. <br>
Promotional packages on appropriate technologies for farmers in rural areas. <br>

