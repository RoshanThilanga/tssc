                                        <h3>Model Fuelwood Plantations for Sustainable Energy Supply and Livelihood Development</h3>
                                        <div class="pragraph">
                                            <p  style="font-size: 15px">Whilst the Forest Department manages and maintains these forests, they have joined hands with the Sustainable Biomass Energy Project to establish model fuelwood plantations in lands belonging to the Department. These plantations would prevent further damage to these natural forests.

                                                The Project also collaborates with other government institutions such as the Coconut Cultivation Board (CCB) and the Rubber Research Institute of Sri Lanka (RRISL) (through their own lands and smallholder lands) to establish and promote these model fuelwood plantations. This Project supported provision to establish fuelwood plant nurseries, fuelwood plantations in buffer zones, and to enhance the capacity of fuelwood research through the establishment of a fuelwood arboretum. Additionally, model fuelwood plantations with NGOs and CBOs (in various parts of the country) have been established with the support of the Project to protect forest buffer zones from illegal felling while providing a sustainable fuelwood supply for these communities. These model projects also support families through livelihood enhancement.

                                                Perennial tree crops such as fruits and spices yield a significant volume of biomass during the application of improved crop management practices such as pruning and harvesting. This method is deemed sustainable as it utilizes by-products of forests or tree-crop management for energy generation while improving tree health. These tree crops also increase yields as they can be produced in large quantities and regrow rapidly after each harvest.

                                                The sustainable fuelwood production models fall under five different categories and aid decision makers, investors and land owners develop necessary policies, take decisions on investments and land use sustainably, economically and efficiently.

