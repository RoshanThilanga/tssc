                            <h6> DISK CHIPPERS</h6>
                            <h3>What are disk chippers?</h3>
                            <p>Disk chippers consist of a large diameter steel disk, generally set either at right angles to the direction of feed, or at an angle, that incorporates one or more hardened chopping blades, each with an associated slot in the disk. The disk rotates at high speed and the blade cuts regular chips from the end of the biomass as it is fed in, usually by a ridged roller system. A fixed steel anvil or knife in the throat of the chipper forms the opposing cutting edge. The knives may be set radially on the disk, or at a slight offset. They are usually straight, but may be curved. On the back face of the disk there may be paddles to generate airflow to eject the chips and they may incorporate serrations to act as sliver breakers to attempt to prevent the ejection of long slivers.
                                <br></p>

                            <h3>Characteristics</h3>
                            <p>The size of chip produced by most disk chippers is principally determined by the number of knives and their settings, by the rotational speed of the disk and the material feed speed, which is usually determined by the feed roller speed. Varying these parameters or removing one or more blades in a multi-blade unit and blanking off the associated slot allows chip dimensions to be adjusted. Some disk chipper can have difficulties with thin, whippy material especially when it is fed in at a slight angle, as this can lead to the material being torn rather than cut cleanly, producing long slivers which can cause bridging in fuel feed systems.


                                <br></p>
                            <h3>Technical</h3>
                            <p>Blades are replaceable and can generally be re-sharpened. Blades can be blunted or damaged by dirt, grit, inclusions, ice or frozen timber, and in high throughput situations can require sharpening or replacing more than once a day. Blunt blades can lead to irregular chip sizes, more slivers and greater power requirements. It is highly advisable to ensure a supply of spare blades so that they can quickly be replaced as required and sharpened at convenience. One design uses circular blades that can be rotated when one section becomes blunt to allow a new, sharp face to be presented.


                                <br></p>

                            <h6> DRUM CHIPPERS</h6>
                            <h3>What are drum chippers?</h3>
                            <p>
                                These use a rotating drum with one or more hardened blades, cutting onto an anvil(s) mounted in the throat of the feed. The drum may be parallel sided or waisted in the middle. The feed mechanisms are similar to those of disk chippers or may use a conveyor or gravity. A separate fan may be incorporated, or the blades themselves may generate the airflow to blow the chips out.
                                <br></p>
                            <h3>Characteristics</h3>
                            <p>Chip size is determined by drum speed, feed speed and the number of blades. The axis of the drum is at right angles to the direction of feed, and the whole width can be used. If material can twist in the throat during infeed it can result in long slivers. One design uses a drum that is tapered to the centre, partly to try to minimise this. High quality chips can be achieved with some designs, particularly in combination with a screen. Drum chippers are flexible in being able to handle a range of different material, including twiggy material and round wood.
                                <br></p>


                            <h3>Technical</h3>
                            <p>As with disk chippers, blades are replaceable and can generally be re-sharpened. Blades can be blunted or damaged by dirt, stones inclusions or ice, and in high throughput situations can require sharpening or replacing up to several times a day. Blunt blades can lead to irregular chip sizes, more slivers and greater power requirements. It is highly advisable to ensure a supply of spare blades so that they can quickly be replaced as required and sharpened at convenience.
                                <br></p>



                            <h6> SCREW CHIPPERS</h6>
                            <h3>What are screw chippers?</h3>
                            <p>These operate on a different principle to disk and drum chippers. The cutting blade is a rotating, tapered, helical screw thread with a hardened cutting edge, which cuts against the inside of the housing. The axis of rotation is in line with the direction of feed and the helix draws material into the chipper and is the only feed mechanism required. Chip size is determined by the pitch of the screw, and can be varied by replacing the blade with one of a different pitch. The base of the blade generally includes a disk to generate the airflow to propel the chips out, and may also incorporate a sliver breaker. As material is drawn into the chipper by the action of the screw a separate mechanical feed system is not usually employed.


                                <br></p>
                            <h3>Characteristics</h3>
                            <p>Screw chippers can produce high quality, even sized chips, with good throughput. However they typically require high drive powers. Owing to the feed mechanism it is very difficult to withdraw material once it has started to be drawn in. Screw chippers tend to be quieter in operation than other types.
                                <br></p>


                            <h3>Technical</h3>
                            <p>As with all chippers it is important to ensure that the blade remains sharp, especially if high quality chips are required. The entire helical blade can be replaced, or can be sharpened in situ using a jig provided by the manufacturer. The hardened face can be reconditioned by welding and grinding <br>

